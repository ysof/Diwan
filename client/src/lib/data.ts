/**
 * ديـــوان - Arabic Poetry Data Structure
 * Contains all poems and categories for the website
 */
export interface Poem {
  id: string;
  title: string;
  poet: string;
  // For classic poem layout, content can be an array of verses.
  // Each verse is formatted as: "right hemistich | left hemistich"
  // Fallback to string for any older content.
  content: string | string[];
  categories: string[];
  excerpt: string;
  date: string;
}

export interface Category {
  id: string;
  name: string;
  description: string;
  icon?: string;
}

